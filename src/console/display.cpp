ConsoleDisplay::ConsoleDisplay() {
    framebuffer_.resize(SIZE, 0);
    palette_.fill(ooey::Color(0, 0, 0));
    
    // Default palette color maps
    palette_[0] = ooey::Color(0, 0, 0);       // Black / transparent
    palette_[1] = ooey::Color(255, 255, 255); // White
    palette_[2] = ooey::Color(255, 0, 0);     // Red
    palette_[3] = ooey::Color(0, 255, 0);     // Green
    palette_[4] = ooey::Color(0, 0, 255);     // Blue
}

void ConsoleDisplay::clear(uint32_t color_idx) {
    ooey::Color color = palette_[color_idx & 0xFF];
    for (int i = 0; i < WIDTH * HEIGHT; ++i) {
        framebuffer_[i * 4]     = color.r;
        framebuffer_[i * 4 + 1] = color.g;
        framebuffer_[i * 4 + 2] = color.b;
        framebuffer_[i * 4 + 3] = color.a;
    }
}

void ConsoleDisplay::set_pixel(int x, int y, uint32_t color_idx) {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return;
    
    // Index 0 is transparent, skip rendering
    if ((color_idx & 0xFF) == 0) return;
    
    ooey::Color color = palette_[color_idx & 0xFF];
    int offset = (y * WIDTH + x) * BPP;
    framebuffer_[offset]     = color.r;
    framebuffer_[offset + 1] = color.g;
    framebuffer_[offset + 2] = color.b;
    framebuffer_[offset + 3] = color.a;
}

uint32_t ConsoleDisplay::get_pixel(int x, int y) const {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return 0;
    int offset = (y * WIDTH + x) * BPP;
    
    // Reverse lookup or raw RGB representation. Return index.
    // For simplicity, find the palette index matching the pixel color.
    uint8_t r = framebuffer_[offset];
    uint8_t g = framebuffer_[offset + 1];
    uint8_t b = framebuffer_[offset + 2];
    
    for (size_t i = 0; i < palette_.size(); ++i) {
        if (palette_[i].r == r && palette_[i].g == g && palette_[i].b == b) {
            return i;
        }
    }
    return 0;
}

void ConsoleDisplay::draw_line(int x1, int y1, int x2, int y2, uint32_t color_idx) {
    int dx = std::abs(x2 - x1);
    int dy = std::abs(y2 - y1);
    int sx = (x1 < x2) ? 1 : -1;
    int sy = (y1 < y2) ? 1 : -1;
    int err = dx - dy;

    while (true) {
        set_pixel(x1, y1, color_idx);
        if (x1 == x2 && y1 == y2) break;
        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y1 += sy;
        }
    }
}

void ConsoleDisplay::draw_rect(int x, int y, int w, int h, uint32_t color_idx, bool fill) {
    if (fill) {
        for (int row = y; row < y + h; ++row) {
            for (int col = x; col < x + w; ++col) {
                set_pixel(col, row, color_idx);
            }
        }
    } else {
        // Outline
        for (int col = x; col < x + w; ++col) {
            set_pixel(col, y, color_idx);
            set_pixel(col, y + h - 1, color_idx);
        }
        for (int row = y; row < y + h; ++row) {
            set_pixel(x, row, color_idx);
            set_pixel(x + w - 1, row, color_idx);
        }
    }
}

bool ConsoleDisplay::font_bit_set(char c, int px, int py) const {
    uint8_t uc = static_cast<uint8_t>(c);
    if (uc < 32 || uc > 127) return false;
    uint8_t row_byte = font_8x8[uc - 32][py];
    return (row_byte & (0x80 >> px)) != 0;
}

void ConsoleDisplay::draw_text(int x, int y, const std::string& text, uint32_t color_idx) {
    int curr_x = x;
    for (char c : text) {
        if (c == '\n') {
            y += 8;
            curr_x = x;
            continue;
        }
        for (int py = 0; py < 8; ++py) {
            for (int px = 0; px < 8; ++px) {
                if (font_bit_set(c, px, py)) {
                    set_pixel(curr_x + px, y + py, color_idx);
                }
            }
        }
        curr_x += 8;
    }
}

void ConsoleDisplay::draw_sprite(uint32_t sprite_vram_addr, int dx, int dy, uint32_t flags, const std::vector<uint8_t>& vram) {
    if (sprite_vram_addr + 1 >= vram.size()) return;
    
    int sw = vram[sprite_vram_addr];
    int sh = vram[sprite_vram_addr + 1];
    
    if (sprite_vram_addr + 2 + (sw * sh) > vram.size()) return;
    
    bool flip_h = (flags & 1) != 0;
    bool flip_v = (flags & 2) != 0;
    
    for (int sy = 0; sy < sh; ++sy) {
        for (int sx = 0; sx < sw; ++sx) {
            int read_x = flip_h ? (sw - 1 - sx) : sx;
            int read_y = flip_v ? (sh - 1 - sy) : sy;
            
            uint8_t pixel_idx = vram[sprite_vram_addr + 2 + (read_y * sw + read_x)];
            set_pixel(dx + sx, dy + sy, pixel_idx);
        }
    }
}

void ConsoleDisplay::set_palette_color(uint8_t idx, uint8_t r, uint8_t g, uint8_t b) {
    palette_[idx] = ooey::Color(r, g, b);
}

